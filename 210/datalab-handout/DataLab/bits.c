/* 
 * bitOr - x|y using only ~ and & 
 *   Example: bitOr(6, 5) = 7
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitOr(int x, int y) {
  /*
   * uses DeMorgan's law bitwise on x and y to represent | alternatively usign
   * only ~ and & operators
   */
  return ~((~x)&(~y));
}

/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  /*
   * shifts the byte to become the least significant byte and then uses a
   * mask in order to return the value of just that byte
   */
  return (x>>(n<<3))&0xFF;
}

/* 
 * logicalShift - shift x to the right by n, using a logical shift
 *   Can assume that 0 <= n <= 31
 *   Examples: logicalShift(0x87654321,4) = 0x08765432
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3 
 */
int logicalShift(int x, int n){
  /*
   * Shifts x arithmetically and then uses the appropriate mask to ensure that 
   * the more significant bits added are represented as 0s, even if a shift doesn't
   * occur on x (n=0)
   */
  int shifted_x = x>>n;
  int mask = ~(((~0)<<(31+(~n+1))<<1));
  return shifted_x & mask;
}

/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
  /*
   * Exploits the nature of an algorithm which splits the more significant half of 
   * the number from the less significant half and then uses xor to cancel out any
   * pairs of 1s within the bit until the number can't be split anymore
   */
  int left_shift = x>>16;
  x = x^left_shift;
  left_shift = x>>8;
  x = x^left_shift;
  left_shift = x>>4;
  x = x^left_shift;
  left_shift = x>>2;
  x = x^left_shift;
  left_shift = x>>1;
  x = x^left_shift;
  return x&1;
}

/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  /*
   * Force the value at x to become an n bit integer value by shifting it left until there are
   * only n bits of the original bits that still remain. The shift it back right (sign extend) and use
   * xor to determine whether the original x value is the same as the shifted x value that was forced
   * to be n bits.
   */
  int shift = 32+(~n+1);
  int shift_x = (x<<shift)>>shift;
  return !(shift_x^x);
}

/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
  /*
   * Represent the rules for negating an integer in twos complement notation
   */
  return (~x)+1;
}

/* 
 * isPositive - return 1 if x > 0, return 0 otherwise 
 *   Example: isPositive(-1) = 0.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 8
 *   Rating: 3
 */
int isPositive(int x) {
  int neg_x = (~x)+1;
  return (!(x>>31))&!!(x^neg_x);
}

/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  /*
   * Uses the value of the most significant bit to return a 0 everytime an overflow occurs
   * through isolating the msb and determining whether two negative values yield a positive 
   * value or two positive values yield a negative value (both cases for overflow)
   */
  int shift_x = x>>31;
  int shift_y = y>>31;
  int shift_sum = (x+y)>>31;
  return !((shift_x&shift_y&~shift_sum)|((~shift_x)&(~shift_y)&shift_sum));  
}
